    private class ServerTableTableCellRenderer extends DefaultTableCellRenderer {

        @Override
        public Component getTableCellRendererComponent(JTable table,
                Object value,
                boolean isSelected,
                boolean hasFocus,
                int rowIndex,
                int vColIndex) {
            
            Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, rowIndex, vColIndex);

            if (table.getModel().getValueAt(rowIndex, 3).equals("PSK>APRS random") ||
                    table.getModel().getValueAt(rowIndex, 2).equals("10147,0")) {
                // setForeground(Color.RED);
                c.setForeground(new Color(255, 0, 0));
                Font current = getFont();
                c.setFont(current.deriveFont(Font.BOLD));
            } else {
                c.setForeground(Color.BLACK);
            }

            return c;
            
        }

        @Override
        public void validate() {}
        @Override
        public void revalidate() {}
        @Override
        protected void firePropertyChange(String propertyName, Object oldValue, Object newValue) {}
        @Override
	public void firePropertyChange(String propertyName, boolean oldValue, boolean newValue) {}

    }

