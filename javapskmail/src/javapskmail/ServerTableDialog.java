    private void btnDelRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelRowActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model;

        if (tblServer.getModel() instanceof DefaultTableModel) {
            model = (DefaultTableModel) tblServer.getModel();
        } else {
            return;
        }

        int numRows = tblServer.getSelectedRows().length;

        for (int i = 0; i < numRows; i++) {
            model.removeRow(tblServer.getSelectedRow());
        }
        
    }//GEN-LAST:event_btnDelRowActionPerformed

