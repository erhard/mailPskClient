    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (pskmaillogic != null) {
            String mystring;
            pskmaillogic.getParentUI().setBoardWX("/Wind:" + spnWind.getValue() + "," + cboWindDir.getSelectedItem() +
                "/Sea:" + spnSea.getValue() + "," + cboSeaDir.getSelectedItem() +
                "/Hp:" + spnPressure.getValue() + "/T:" + spnTemp.getValue() + cboWeather.getSelectedItem() +
                "/A:" + cboAntenna.getSelectedItem() + "/P:" + spnPower.getValue());
            mystring = pskmaillogic.getParentUI().getBoardWX();
            pskmaillogic.myconfig.setPreference("WX", mystring);
            pskmaillogic.myarq.setBoardWX(mystring);
        }
        setVisible(false);
    }//GEN-LAST:event_btnSaveActionPerformed

